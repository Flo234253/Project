package com.example.project.Model;
/*This interface will get and set the following: name, email and password*/
public interface Users {

    /*Set the name*/
    String getName();

    // Set for the name
    void setName(String aName);

    // Get for the password
    String getPassword();

    // Set for the password
    void setPassword(String aPassword);

    // Get for the email
    String getEmail();

    // Set for the email
    void setEmail(String aEmail);
}

